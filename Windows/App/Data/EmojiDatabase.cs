    public EmojiItem[] Search(string query) {
        if (string.IsNullOrWhiteSpace(query)) return Array.Empty<EmojiItem>();

        var lowerQuery = query.ToLowerInvariant();
        var normalizedQuery = VietnameseTextUtilities.Normalize(lowerQuery);

        var searchTerms = new HashSet<string>(StringComparer.OrdinalIgnoreCase) {
            lowerQuery, normalizedQuery
        };

        if (VietnameseSynonyms.Map.TryGetValue(lowerQuery, out var syns1))
            foreach (var s in syns1) searchTerms.Add(s);
        if (VietnameseSynonyms.Map.TryGetValue(normalizedQuery, out var syns2))
            foreach (var s in syns2) searchTerms.Add(s);

        var scoredResults = new List<(EmojiItem Emoji, int Score)>();
        var addedEmojis = new HashSet<string>();
        var termsArray = searchTerms.ToArray();

        foreach (var (_, _, emojis) in Categories) {
            foreach (var emoji in emojis) {
                if (addedEmojis.Contains(emoji.Emoji)) continue;

                var score = CalculateMatchScore(emoji, termsArray);
                if (score > 0) {
                    var freqBoost = Math.Min(
                        _emojiFrequency.GetValueOrDefault(emoji.Emoji, 0), 10) * 5;
                    scoredResults.Add((emoji, score + freqBoost));
                    addedEmojis.Add(emoji.Emoji);
                }
            }
        }

        scoredResults.Sort((a, b) => b.Score.CompareTo(a.Score));
        return scoredResults.Select(r => r.Emoji).ToArray();
    }

    private int CalculateMatchScore(EmojiItem emoji, string[] searchTerms) {
        var maxScore = 0;
        var nameLower = emoji.Name.ToLowerInvariant();
        var nameNormalized = VietnameseTextUtilities.Normalize(nameLower);
        var keywordsLower = emoji.Keywords.Select(k => k.ToLowerInvariant()).ToArray();
        var keywordsNormalized = emoji.Keywords
            .Select(k => VietnameseTextUtilities.Normalize(k.ToLowerInvariant())).ToArray();

        foreach (var term in searchTerms) {
            var termNormalized = VietnameseTextUtilities.Normalize(term);

            var nameScore = MatchScore(term, termNormalized, nameLower, nameNormalized);
            maxScore = Math.Max(maxScore, nameScore);

            for (var i = 0; i < keywordsLower.Length; i++) {
                var kwScore = MatchScore(term, termNormalized,
                    keywordsLower[i], keywordsNormalized[i]);
                if (kwScore > 0)
                    maxScore = Math.Max(maxScore, kwScore - 5);
            }
        }
        return maxScore;
    }

    private static int MatchScore(string query, string queryNormalized,
                                   string target, string targetNormalized) {
        if (target == query || targetNormalized == queryNormalized) return 100;
        if (target.StartsWith(query) || targetNormalized.StartsWith(queryNormalized)) return 80;

        var targetWords = target.Split(' ');
        foreach (var word in targetWords)
            if (word == query || word.StartsWith(query)) return 60;

        var normalizedWords = targetNormalized.Split(' ');
        foreach (var word in normalizedWords)
            if (word == queryNormalized || word.StartsWith(queryNormalized)) return 60;

        if (target.Contains(query) || targetNormalized.Contains(queryNormalized)) return 40;
        return 0;
    }

    public void RecordUsage(string emoji) {
        _recentEmojis.Remove(emoji);
        _recentEmojis.Insert(0, emoji);
        if (_recentEmojis.Count > MaxRecentEmojis)
            _recentEmojis = _recentEmojis.Take(MaxRecentEmojis).ToList();

        _emojiFrequency[emoji] = _emojiFrequency.GetValueOrDefault(emoji, 0) + 1;
        SaveData();
    }

    public List<string> GetRecentEmojis(int limit = 20) =>
        _recentEmojis.Take(limit).ToList();

    public List<string> GetFrequentlyUsedEmojis(int limit = 20) =>
        _emojiFrequency.OrderByDescending(kv => kv.Value)
            .Take(limit)
            .Select(kv => kv.Key)
            .ToList();

    public EmojiItem? GetEmojiItem(string emoji) {
        foreach (var (_, _, emojis) in Categories)
            foreach (var item in emojis)
                if (item.Emoji == emoji) return item;
        return null;
    }

    private void LoadData() {
        try {
            if (!File.Exists(_dataFilePath)) return;
            var json = File.ReadAllText(_dataFilePath);
            var data = JsonSerializer.Deserialize<PickerData>(json);
            if (data == null) return;
            _recentEmojis = data.RecentEmojis ?? new();
            _emojiFrequency = data.EmojiFrequency ?? new();
        } catch {
            // Ignore corrupt data
        }
    }

    private void SaveData() {
        try {
            var data = new PickerData {
                RecentEmojis = _recentEmojis,
                EmojiFrequency = _emojiFrequency
            };
            var json = JsonSerializer.Serialize(data, new JsonSerializerOptions {
                WriteIndented = true
            });
            File.WriteAllText(_dataFilePath, json);
        } catch {
            // Ignore write errors
        }
    }

    private sealed class PickerData {
        [JsonPropertyName("recent_emojis")]
        public List<string> RecentEmojis { get; set; } = new();

        [JsonPropertyName("emoji_frequency")]
        public Dictionary<string, int> EmojiFrequency { get; set; } = new();
    }
